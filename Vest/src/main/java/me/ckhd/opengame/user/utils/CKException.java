package me.ckhd.opengame.user.utils;

public class CKException extends Exception{

	private static final long serialVersionUID = -3388001291604660055L;
	private int code;
	private String msg;
	public CKException(int code, String msg) {
		super();
		this.code = code;
		this.msg = msg;
	}
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
}
