package me.ckhd.opengame.drds.entity;

public enum Stat {
	SECOND(1,"second_day",1),
	THIRD(2,"third_day",2),
	SEVENTH(3,"seventh_day",6),
	FIFTEENTH(4,"fifteenth_day",14),
	THIRTIETH(5,"thirtieth_day",29);
	int type;
	String field;
	int startDate;
	
	Stat(int type,String field,int startDate){
		this.type=type;
		this.field=field;
		this.startDate=startDate;
	}

	public int getType() {
		return type;
	}

	public String getField() {
		return field;
	}

	public int getStartDate() {
		return startDate;
	}
}
