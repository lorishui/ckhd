package me.ckhd.opengame.app.entity;

import java.util.Date;

import me.ckhd.opengame.common.persistence.DataEntity;

/**
 * @ClassName SendMobileMessage
 * @Description 短信发送记录表
 * @author liupei
 * @Date 2017年6月12日 上午11:38:49
 * @version 1.0.0
 */
public class SendMobileMessage extends DataEntity<SendMobileMessage>{
    
    /**
     * @Field @serialVersionUID :序列化使用
     */
    private static final long serialVersionUID = -8556332146086581092L;
    /**手机号码*/
    private String mobile;
    /**手机短信发送提供方*/
    private String mark;
    /**手机短信发送时间*/
    private Date createDate;
    
    public String getMobile() {
        return mobile;
    }
    
    public void setMobile(String mobile) {
        this.mobile = mobile;
    }
    
    public String getMark() {
        return mark;
    }
    
    public void setMark(String mark) {
        this.mark = mark;
    }
    
    public Date getCreateDate() {
        return createDate;
    }
    
    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }
    
}
